		<blockquote>Вот, для наглядности - эффективность Силы Кумкумади</blockquote>
		<h3><br />Прошло 4 месяца</h3>
		<p>С моего чудесного преображения прошло 4 месяца.  Что изменилось с тех пор?  Скажу однозначно – я еще больше полюбила и жизнь, и саму себя в ней. Я Больше стала смеяться и радоваться даже пустякам.   Поменяла цвет волос и прическу на более молодежные. Муж говорит, что я на 15 лет помолодела лицом и на 30 лет душой! </p>
		<p><img src="files/img/9.jpg" alt="" class="center"></p>
		<blockquote>С помолодевшим лицом можно вспомнить молодость и подурачиться. Это не будет выглядеть нелепо</blockquote>
		<p>Омолаживающий эффект от Силы Кумкумади не пропал. В аннотации разработчики обещают его сохранения на год-полтора! Больше, чем у ботокса. И за куда меньшие деньги! Наука не стоит на месте. </p>
