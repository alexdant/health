		<h1 style="color:#00C;"><strong>ЕЛЕНА  МИХАЙЛОВА:   Как в 56 помолодеть без пластических  операций и уколов ботокса!</strong></h1>
		<p>Хочу рассказать свою историю по омоложению. Может она пригодится другим женщинам., мечтающим и после пятидесяти выглядеть красиво и привлекательно. Мой способ хорош тем, что он эффективен, недорог и не требует много времени. </p>
		<h3><br />Вначале совсем коротко о себе </h3>
		<p>Зовут Еленой, живу в маленьком сибирском поселке Таежный. Работаю учительницей. Замужем. Дочь уже взрослая и подарила нам с мужем прекрасную внученьку Машеньку. </p>
		<p><img src="files/img/1.jpg" alt="" class="center"></p>
		<blockquote>Дочь нам с мужем подарила чудесную внученьку Машеньку</blockquote>
		<p>Почему решила омолодить лицо? Да просто потому, что хотела быть красивой и по-прежнему нравиться мужу. Ведь это так естественно для женщины! «Красота спасет мир»! – я верю в эту формулу. </p>
		<p>К своим 56 годам я выглядела, как практически все наши российские женщины в этом возрасте. </p>
		<p><img src="files/img/2.jpg" alt="" class="center"></p>
		<blockquote>Мое лицо еще 4 месяца назад. Можно и не комментировать. Очки стали привычным аксессуаром, что бы спрятать мешки под глазами</blockquote>
		<h3><br />Как я помолодела</h3>
		<p>К вопросу омоложения я подошла, как любая учительница, которая готовится к незнакомой ей теме – обстоятельно изучила ее.</p>
		<p>Радикальных способов омоложения в принципе существует только три:</p>
		<p>•	Аппаратная косметология  – лазерный пиллинг, ультразвук, магниты и т.д.<br /> 
			•	Уколы ботоксом.<br />
			•	Пластическая хирургия.
		</p>
		<p><strong>Первый способ мне не подходил</strong> по причине отсутствия такой услуги в нашем Таежном. А до областного центра – Красноярска (больше 250 км!) – не наездишься на 10-15 сеансов. Да и дорого все это. Не для учительской зарплаты. </p>
		<p><strong>Уколы ботокса я боялась</strong>. Моя подружка их сделала в одном Красноярском косметическом центре, так у нее под глазами так распухло – просто  ужас. Три недели в школу не ходила. <br />
			К тому же у меня аллергия на кучу лекарственных средств – начиная от безобидного  нафазолина от насморка и заканчивая антибиотиками. Так что я не хотела рисковать. И опять же цена! 
		</p>
		<p><strong>Про пластическую операцию я просто не думала</strong>. За деньги на эту операцию можно весь наш поселок Таежный купить. </p>
		<p>Немножко я погоревала, но раз нет, значит нет. </p>
		<h3><br />Мне помог случай</h3>
		<p><br /><img src="files/img/6.jpg" alt="" class="center"></p>
		<blockquote>Спасибо Алле Борисовне за подсказку!</blockquote>
		<p>И тут я увидела по телевизору передачу Малахова «Пусть говорят» с Аллой Пугачевой. Передачу снимали у нее дома в канун ее дня рождения.  </p>
		<p>Как обычно, Малахов говорит Алле дежурные комплименты, как она хорошо выглядит и вдруг, неожиданно, Примадонна отвечает нестандартно: </p>
		<p><strong>– Спасибо американцам! В своем возрасте я уже боюсь делать пластические операции. </strong></p>
		<p>Оказывается, 7 лет назад она чуть не умерла в швейцарской клинике от пластической хирургии и надолго прекратила опасные эксперименты над внешностью. Ботокс не колола, потому что лицо как у куклы, никакой мимики. Но, слава Богу, японский институт красоты, предложили еще лучше средство – масло <strong>"Сила Кумкумади"</strong>. Даже с большей эффективностью чем ботокс, но абсолютно безопасно и никаких инъекций! И в доказательство его эффективности показывает свои фото   3-х месячной давности.</p>
		<p><img src="files/img/5.jpg" alt="" class="center"></p>
		<blockquote>Разница, конечно, существенная!</blockquote>
		<p>Потом Малахов начал расспрашивать Аллу за ее маленьких детей, за Кристину, внуков и т.д.  Мне это было не интересно. Я уже мечтала о Силе Кумкумадие! </p>
		<p>Стала искать в Интернете информацию о нем. И нашла! Его в этом году сертифицировал наш  Государственный научный центр дерматовенерологии и косметологии" и теперь он доступен для заказа <a href="https://alexdant.github.io/health/kum" onClick="https://alexdant.github.io/health/kum" target="_blank"><strong>на официальном сайте</strong></a>!  
		<p><strong>Что я тут же и сделала! </strong>Сразу заказала! Раз масло похвалила Алла Пугачева, он станет сразу популярным и наши бизнесмены могут и цену поднять.</p>
		<p>И вот через 10 дней долгожданная посылка у меня. Как и написано в инструкции, я стала использовать масло перед сном.  Перемены увидела уже практически на следующий день! Заметно разгладились «гусиные лапки». Существенно уменьшились носогубные складки, морщины на лбу, посветлела кожа, <strong>подтянулся овал лица</strong>.  Как я радовалась! И каждый день я отмечала новые радостные изменения на своем лице.  </p>
		<p>Это была фантастикой!<strong> И я решила эту фантастику запечатлеть</strong>, периодически крупным планом снимая свое лицо.  Вот, что из этого получилось.</p>
